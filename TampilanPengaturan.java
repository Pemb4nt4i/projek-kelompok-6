    private void BtnBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBackupActionPerformed
            System.out.println("Tab Backup & Restore dipilih");
    
    int choice = JOptionPane.showOptionDialog(this,
        "Pilih aksi yang ingin dilakukan:",
        "Backup & Restore",
        JOptionPane.YES_NO_CANCEL_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        null,
        new String[]{"Backup Data", "Restore Data", "Batal"},
        "Backup Data");
        
    switch(choice) {
        case 0: // Backup
            System.out.println("Memulai proses backup...");
            JOptionPane.showMessageDialog(this, 
                "Backup data berhasil!\nFile disimpan di: backup_" + 
                new java.text.SimpleDateFormat("yyyyMMdd_HHmmss").format(new java.util.Date()) + ".sql",
                "Backup Berhasil", 
                JOptionPane.INFORMATION_MESSAGE);
            break;
        case 1: // Restore
            System.out.println("Memulai proses restore...");
            // Implementasi file chooser untuk pilih file backup
            JOptionPane.showMessageDialog(this, 
                "Fitur restore akan segera tersedia",
                "Info", 
                JOptionPane.INFORMATION_MESSAGE);
            break;
        default:
            System.out.println("Aksi dibatalkan");
    }
    }//GEN-LAST:event_BtnBackupActionPerformed

