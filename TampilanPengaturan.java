    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
            try {
        // Ambil data dari form
        String namaToko = jTextField4.getText().trim();
        String alamat = jTextField2.getText().trim();
        String noTelepon = jTextField1.getText().trim();
        String email = jTextField3.getText().trim();
        String tema = jComboBox1.getSelectedItem().toString();
        String bahasa = jComboBox2.getSelectedItem().toString();
        
        // Validasi input
        if (namaToko.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Toko tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alamat tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validasi email jika diisi
        if (!email.isEmpty() && !email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Format email tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Simpan pengaturan (implementasi sesuai kebutuhan)
        // Misalnya simpan ke database atau file konfigurasi
        
        System.out.println("Menyimpan pengaturan:");
        System.out.println("Nama Toko: " + namaToko);
        System.out.println("Alamat: " + alamat);
        System.out.println("No Telepon: " + noTelepon);
        System.out.println("Email: " + email);
        System.out.println("Tema: " + tema);
        System.out.println("Bahasa: " + bahasa);
        
        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(this, 
            "Pengaturan berhasil disimpan!", 
            "Sukses", 
            JOptionPane.INFORMATION_MESSAGE);
            
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Terjadi kesalahan saat menyimpan: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        System.err.println("Error: " + e.getMessage());
    }
    }//GEN-LAST:event_BtnSimpanActionPerformed

